class ProfileInfo {
  final String name;
  final String email;
  final String phone;
  final String aadhaar;
  final String businessId;
  final String address;
  final bool companyVerified;

  const ProfileInfo({
    required this.name,
    required this.email,
    required this.phone,
    required this.aadhaar,
    required this.businessId,
    required this.address,
    required this.companyVerified,
  });
}

